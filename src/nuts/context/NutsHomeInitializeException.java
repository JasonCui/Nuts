package nuts.context;

/**
 * 
 * @author jicui
 *
 */
public class NutsHomeInitializeException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -3320954710172106695L;

	public NutsHomeInitializeException() {
		super();
	}

	public NutsHomeInitializeException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NutsHomeInitializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public NutsHomeInitializeException(String message) {
		super(message);
	}

	public NutsHomeInitializeException(Throwable cause) {
		super(cause);
	}
	
	

}
